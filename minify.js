function minifyLua(code) {
    // Удаляем комментарии, лишние пробелы, табы и переносы строк
    return code
      .replace(/--.*$/gm, '') // Удаление комментариев
      .replace(/\s+/g, ' ')   // Все пробелы/табы/переносы → один пробел
      .replace(/\s*([=(),])\s*/g, '$1') // Удаление пробелов вокруг =, (, ), ,
      .trim();                // Удаление пробелов в начале и конце
  }

  
module.exports.minifyLua = minifyLua